#include "pch.h"
#include "CxCalculate.h"


double CxCalculate::Add(double i_No1, double i_No2)
{
	return i_No1 + i_No2;
}
double CxCalculate::Subtract(double i_No1, double i_No2)
{
	return i_No1 - i_No2;
}

double CxCalculate::Multiply(double i_No1, double i_No2)
{
	return i_No1 * i_No2;
}
double CxCalculate::Divide(double i_No1, double i_No2)
{
	return i_No1 / i_No2;
}